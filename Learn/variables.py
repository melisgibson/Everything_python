# Setting variable my_str
my_str = "This is a simple string" 
print(my_str)

# Reassigning the variable my_str
my_str = "This is a reassigned string" 
print(my_str)

# adding to the string
my_str += " addition" 
print(my_str) 

# adding to the string using a different method
my_str = my_str + " addition"  
print(my_str)

# reassigning variable as an integer
my_str = 1  
print(my_str)
